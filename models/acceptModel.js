const mongoose = require('mongoose');

const acceptSchema = new mongoose.Schema({
  userid: {
    type: String,
    required: true,
  },
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  summary: {
    type: String,
    required: true,
  },
  time: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
  lawyername: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model('Accept', acceptSchema);
